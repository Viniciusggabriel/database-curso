ALTER TABLE CONSULTA ADD CONSTRAINT CONSTRAINT_FK_MEDICO FOREIGN KEY (FK_MEDICO) REFERENCES MEDICO(ID_MEDICO);
ALTER TABLE CONSULTA ADD CONSTRAINT CONSTRAINT_FK_HOSPITAL FOREIGN KEY  (FK_HOSPITAL) REFERENCES HOSPITAL(ID_HOSPITAL);
ALTER TABLE INTERNACAO ADD CONSTRAINT CONSTRAINT_FK_CONSULTA FOREIGN KEY (FK_CONSULTA) REFERENCES CONSULTA(ID_CONSULTA);