CREATE DATABASE PROJETO;

USE PROJETO;

CREATE TABLE
    CURSOS (
        ID_CURSO INT PRIMARY KEY AUTO_INCREMENT,
        NOME VARCHAR(30) NOT NULL,
        HORAS INT (3) NOT NULL,
        VALOR FLOAT (10, 2) NOT NULL -- CABE 8 ANTES DA VIRGULA E 2 DEPOIS
    );

